    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
        // TODO add your handling code here:
        //System.out.println("inside action performed of subject");
        if(jcSubject.getItemCount()==0 || jcSubject.getSelectedItem().equals(""))
        {
            jcExamId.removeAllItems();
            return;
        }
        jcExamId.removeAllItems();
        String selection=jcSubject.getSelectedItem().toString();
        try
        {
            List<String> examIdList=ExamDAO.getExamIdBySubject(selection);
            if(examIdList.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No exam exists for this subject, please select another subject", "Error!", JOptionPane.ERROR_MESSAGE);
                jcSubject.setSelectedIndex(0);
                return;
            }
            for(String str:examIdList)
            {
                jcExamId.addItem(str);
            }
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Edit Paper DB Error","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcSubjectActionPerformed

