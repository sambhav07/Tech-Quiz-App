    private void btnEditTheQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditTheQuestionsActionPerformed
        // TODO add your handling code here:
        if(jcSubject.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null, "Please select a subject", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int ans=JOptionPane.showConfirmDialog(null, "Are you sure you want to edit "+jcSubject.getSelectedItem()+" exam with exam id "+jcExamId.getSelectedItem(), "Confirm", JOptionPane.YES_NO_OPTION);
        if(ans==JOptionPane.YES_OPTION)
        {
             String examId=jcExamId.getSelectedItem().toString();
             String language=jcSubject.getSelectedItem().toString();
             int qCount=0;
             try
             {
                 qCount=ExamDAO.getQuestionCountByExam(examId);
             }
             catch(SQLException ex)
             {
                 JOptionPane.showMessageDialog(null, "DB Error", "Error!", JOptionPane.ERROR_MESSAGE);
             }
             Exam exam=new Exam(examId,language,qCount);
             EditQuestionsFrame eqf=new EditQuestionsFrame(exam);
             eqf.setVisible(true);
             this.dispose();
             
        }
        
    }//GEN-LAST:event_btnEditTheQuestionsActionPerformed

