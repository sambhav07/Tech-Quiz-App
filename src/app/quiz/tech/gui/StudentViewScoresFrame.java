    private void jcSelectExamIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSelectExamIdActionPerformed
        // TODO add your handling code here:
        if( jcSelectExamId.getItemCount()==0 || jcSelectExamId.getSelectedItem().equals("") || jcSelectExamId.getSelectedIndex()==0)
        {
            langTextField.setText("");
            percTextField.setText("");
            return;
        }
        try{
            StudentScore ss=PerformanceDAO.getScore(UserProfile.getUserName(), jcSelectExamId.getSelectedItem().toString());
            langTextField.setText(ss.getLanguage());
            percTextField.setText(String.valueOf(ss.getPer()));
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Student View Student Score DB Error!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcSelectExamIdActionPerformed

