    private void loadExamId() {
        try
        {
            examList=PerformanceDAO.getAllExamId(UserProfile.getUserName());
            if(examList.isEmpty())
            {
                jcSelectExamId.removeAllItems();
                langTextField.setText("");
                percTextField.setText("");
                JOptionPane.showMessageDialog(null, "Sorry! No exam has been given by you", "No Records Found!", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                jcSelectExamId.addItem("Select Exam Id");
                for(String exam: examList)
                {
                    jcSelectExamId.addItem(exam);
                }
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Student View Student Score DB Error!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
