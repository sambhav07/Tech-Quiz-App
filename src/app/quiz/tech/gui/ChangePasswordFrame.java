    private void btnUpdatePwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePwdActionPerformed
        // TODO add your handling code here:
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null, "Please fill all the fields!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!validatePasswords())
        {
            JOptionPane.showMessageDialog(null, "Passwords doesn't match, retry!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int response=JOptionPane.showConfirmDialog(null, "Are you sure?", "Confirm!",JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(response==JOptionPane.YES_OPTION)
        {
            try
            {
                String pwd=String.valueOf(pwdTextField.getPassword());
                User u=new User(UserProfile.getUserName(),pwd,UserProfile.getUserType());
                boolean res=UserDAO.updateUserPassword(u);
                if(res==true)
                {
                    JOptionPane.showMessageDialog(null, "Password Updated Successfully!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                    uidTextField.setText("");
                    pwdTextField.setText("");
                    rpwdTextField.setText("");
                    showCheckBox.setSelected(false);
                    uidTextField.requestFocus();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Password cannot be updated", "Error!", JOptionPane.ERROR_MESSAGE);
                }
            }catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Password Update DB Error", "Error!", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnUpdatePwdActionPerformed

