    private boolean validateInput() {
        if(uidTextField.getText().isEmpty() || pwdTextField.getPassword().length==0 || rpwdTextField.getPassword().length==0)
        {
            return false;
        }
        return true;
    }

    private boolean validatePasswords() {
        if(pwdTextField.getPassword().length!=rpwdTextField.getPassword().length)
        {
            return false;
        }else if(!(String.valueOf(pwdTextField.getPassword()).equals(String.valueOf(rpwdTextField.getPassword()))))
        {
            return false;
        }
        return true;
    }
