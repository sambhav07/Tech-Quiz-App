    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null, "Please fill all the fields", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String correctAnswer=options.get(correctOption);
        Question ques=new Question(exam.getExamId(),qno,question,option1,option2,option3,option4,correctAnswer,exam.getLanguage());
        qstore.addQuestion(ques);
        clearAll();
        titleLabel.setText(exam.getTotalQuestions()-qno+" QUESTIONS REMAINING");
        qno++;
        if(qno>exam.getTotalQuestions())
        {
            disableAll();
            JOptionPane.showMessageDialog(null, "Your questions have all been prepared successfully.\n Press the DONE button to add them to the database", "Exam Added!", JOptionPane.INFORMATION_MESSAGE);
            System.out.println(qstore);
        }
        else
        {
            qnoLabel.setText("Question no : "+qno);
        }
    }//GEN-LAST:event_btnNextActionPerformed

