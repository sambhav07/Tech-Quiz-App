    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
        
        if(qno<=exam.getTotalQuestions())
        {
            int remQues=exam.getTotalQuestions()-qno+1;
            JOptionPane.showMessageDialog(null, "You still have "+remQues+" questions remaining", "Fill all the question!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            boolean examSaved=ExamDAO.addExam(exam);
            if(!examSaved)
            {
                JOptionPane.showMessageDialog(null, "Exam not added in the DB", "Try Again!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            QuestionDAO.addQuestion(qstore);
            AdminOptionsFrame aof=new AdminOptionsFrame();
            this.dispose();
            aof.setVisible(true);
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Set Questions Error", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDoneActionPerformed

